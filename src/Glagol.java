package autopoet;

public class Glagol extends Word{

	private String vremya;
	private String chislo;
	private String rod;
	private boolean perekhod;
	
	public String getVremya() {
		return vremya;
	}
	public void setVremya(String vremya) {
		this.vremya = vremya;
	}
	public String getChislo() {
		return chislo;
	}
	public void setChislo(String chislo) {
		this.chislo = chislo;
	}
	public String getRod() {
		return rod;
	}
	public void setRod(String rod) {
		this.rod = rod;
	}
	public boolean isPerekhod() {
		return perekhod;
	}
	public void setPerekhod(boolean perekhod) {
		this.perekhod = perekhod;
	}
	
	
	
}
